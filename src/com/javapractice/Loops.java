package com.javapractice;

public class Loops 
{

	public static void main(String[] args) 
	{
		//While Loops
		int i = 1;
		
		while(i<=5)
		{
			System.out.println(i);
			i = i+1;
		}
		
		//For Loops
		
		for(int x=1; x<=10; x++)
		{
			System.out.println(x);
		}
		
		for(i=10; i>=1; i--)
		{
			System.out.println(i);
		}
	}

}
